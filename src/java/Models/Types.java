package Models;



/**
 *
 * @author thanh
 */
public class Types {
    public int id;
    public String name;
    
    public Types() {
        id = 0;
        name = "";
    }


    public Types(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

}
